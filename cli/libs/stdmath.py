def add(num1: int | float, num2: int | float) -> int | float:
    """
    Usage: add <num1> <num2>

    Adds two numbers together.
    """
    return num1 + num2


def sub(num1: int | float, num2: int | float) -> int | float:
    """
    Usage: sub <num1> <num2>

    Subtracts two numbers.
    """
    return num1 - num2
